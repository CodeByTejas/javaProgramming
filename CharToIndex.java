package jan5;

public class CharToIndex {
    public static void main(String[] args) {
        String s = "Tejas";
        System.out.println("Index of a is:" + s.indexOf('T'));
        System.out.println("Index of a is:" + s.indexOf('e'));
        System.out.println("Index of a is:" + s.indexOf('j'));
        System.out.println("Index of a is:" + s.indexOf('a'));
        System.out.println("Index of a is:" + s.indexOf('s'));
    }
}
